            {{-- PRS Basics -------------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="The sport"
                title="PRS Basics"
                description="The fundamentals of precision rifle competition and how matches are run."
            >
                <x-site.faq-item question="What is PRS?">
                    <p>
                        PRS (Precision Rifle Series) is a practical long-range shooting discipline where competitors
                        engage targets at varying distances under time pressure.
                    </p>
                    <p>
                        Matches consist of multiple stages where shooters must hit steel targets from different
                        positions, often using barricades or props. The sport tests a combination of precision, speed,
                        and problem-solving.
                    </p>
                    <p>
                        Targets can range from close distances out to well beyond 1000 metres, and shooters are scored
                        based on the number of successful hits within a set time.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What is PR22?">
                    <p>
                        PR22 is a rimfire version of PRS that uses .22LR rifles.
                    </p>
                    <p>
                        It follows the same principles as centerfire PRS — including stage-based shooting, positional
                        challenges, and time limits — but at shorter distances and with significantly lower cost.
                    </p>
                    <p>
                        This makes PR22 one of the most accessible ways to get started in precision rifle shooting
                        while still developing the same core skills.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What happens at a match?">
                    <p>
                        A match is made up of multiple stages, each with a specific course of fire.
                    </p>
                    <p>
                        Shooters move through stages with a squad and take turns engaging targets under a time limit.
                        Each stage requires shooting from different positions, often using props or natural terrain.
                    </p>
                    <p>
                        The goal is to hit as many targets as possible within the allowed time. The shooter with the
                        highest overall score across all stages wins.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

            {{-- Getting Started --------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="New shooters"
                title="Getting Started"
                description="What you need to know before your first match with PPRC."
            >
                <x-site.faq-item question="What do I need to compete?">
                    <p>
                        To get started, you do not need a perfect setup.
                    </p>
                    <p>At a minimum:</p>
                    <ul>
                        <li>A rifle suitable for precision shooting</li>
                        <li>Quality ammunition</li>
                        <li>A scope with adjustable turrets</li>
                        <li>A bipod or shooting support</li>
                        <li>Eye and ear protection</li>
                    </ul>
                    <p>
                        Most importantly, you need safe firearm handling and a willingness to learn.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="Do I need experience to join?">
                    <p>
                        No. PRS and PR22 are accessible to shooters of all skill levels.
                    </p>
                    <p>
                        Many clubs and matches support new shooters, and the structure of the sport allows you to learn
                        by participating and observing more experienced competitors.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What gear should I bring to my first match?">
                    <p>
                        Keep it simple for your first match and focus on the essentials.
                    </p>
                    <p>Recommended:</p>
                    <ul>
                        <li>Rifle and ammunition</li>
                        <li>Scope</li>
                        <li>Magazine(s)</li>
                        <li>Bipod or shooting bag</li>
                        <li>Eye protection</li>
                        <li>Ear protection</li>
                        <li>Water</li>
                        <li>Weather-appropriate clothing</li>
                        <li>Sun protection such as a cap, sunscreen, and sleeves if needed</li>
                    </ul>
                    <p>
                        Sun protection matters. Even in winter, a full day on the range can be harsh, so bring a cap,
                        sunscreen, and clothing that gives you proper coverage.
                    </p>
                    <p>
                        You do not need advanced gear to start. Focus on safety, learning, and understanding how
                        matches work.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="Can I come watch a match?">
                    <p>
                        In most cases, yes — but it is best to check with the club or match organiser first.
                    </p>
                    <p>
                        Watching a match is one of the best ways to understand how PRS and PR22 work before competing.
                        It gives you a chance to see stage flow, equipment, and match etiquette in person.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

            {{-- Equipment --------------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="Kit"
                title="Equipment"
                description="Calibres, optics, and the gear decisions most shooters ask about first."
            >
                <x-site.faq-item question="What are the best calibres for PRS?">
                    <p>
                        Most competitive PRS shooters use 6mm or 6.5mm calibres.
                    </p>
                    <p>These are popular because they offer:</p>
                    <ul>
                        <li>Lower recoil</li>
                        <li>Good ballistic performance</li>
                        <li>Better ability to spot impacts and misses</li>
                    </ul>
                    <p>
                        Common examples include 6mm Creedmoor, 6 Dasher, and 6.5 Creedmoor.
                    </p>
                    <p>
                        However, you do not need an optimal calibre to start. Many shooters begin with what they have
                        and upgrade over time as they gain experience.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What optics are recommended?">
                    <p>
                        A reliable optic is critical in precision rifle shooting.
                    </p>
                    <p>Look for:</p>
                    <ul>
                        <li>Accurate and repeatable adjustments</li>
                        <li>Clear glass</li>
                        <li>Exposed turrets for dialing</li>
                        <li>A reticle suitable for holdovers</li>
                    </ul>
                    <p>
                        Most shooters use variable magnification scopes in the range of approximately 4&ndash;25x or
                        similar, depending on preference and match requirements.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="First focal plane vs second focal plane — what's the difference?">
                    <p>
                        <strong>First Focal Plane (FFP):</strong>
                        The reticle scales with magnification, meaning holdovers remain accurate at any zoom level.
                        This allows faster corrections during a stage.
                    </p>
                    <p>
                        <strong>Second Focal Plane (SFP):</strong>
                        The reticle stays the same size, and holdovers are only accurate at a specific magnification.
                    </p>
                    <p>
                        In PRS-style shooting, most competitors prefer FFP because stages often require quick
                        adjustments without changing magnification.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

