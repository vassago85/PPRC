            {{-- PRS Basics -------------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="The sport"
                title="PRS Basics"
                description="The fundamentals of precision rifle competition and how matches are run."
            >
                <x-site.faq-item question="What is PRS?">
                    <p>
                        PRS (Precision Rifle Series) is a practical long-range shooting discipline where competitors
                        engage targets at varying distances under time pressure.
                    </p>
                    <p>
                        Matches consist of multiple stages where shooters must hit steel targets from different
                        positions, often using barricades or props. The sport tests a combination of precision, speed,
                        and problem-solving.
                    </p>
                    <p>
                        At PPRC we normally shoot between about 300&nbsp;m and 800&nbsp;m, with stages sometimes
                        stretching up to around 1000&nbsp;m. Elsewhere, PRS can push farther on some ranges — but the
                        core idea is the same: varied distances under time pressure. Shooters are scored on successful
                        hits within the time allowed for each stage.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What is PR22?">
                    <p>
                        PR22 is a rimfire version of PRS that uses .22LR rifles.
                    </p>
                    <p>
                        It follows the same principles as centerfire PRS — stage-based shooting, positional challenges,
                        and time limits — at shorter distances. The difficulty is the same: a serious PR22 match is as
                        demanding as standard PRS.
                    </p>
                    <p>
                        While a top-level rimfire rig can cost as much as a serious centerfire rifle, ammunition is
                        significantly cheaper, so you can shoot and practise much more for less.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What happens at a match?">
                    <p>
                        A match is made up of multiple stages, each with a specific course of fire.
                    </p>
                    <p>
                        Shooters move through stages with a squad and take turns engaging targets under a time limit.
                        Each stage requires shooting from different positions, often using props or natural terrain.
                    </p>
                    <p>
                        The goal is to hit as many targets as possible within the allowed time. The shooter with the
                        highest overall score across all stages wins.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

            {{-- Getting Started --------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="New shooters"
                title="Getting Started"
                description="What you need to know before your first match with PPRC."
            >
                <x-site.faq-item question="What do I need to compete?">
                    <p>
                        To get started, you do not need a perfect setup.
                    </p>
                    <p>At a minimum:</p>
                    <ul>
                        <li>A rifle suitable for precision shooting</li>
                        <li>Quality ammunition</li>
                        <li>A scope with adjustable turrets</li>
                        <li>A <strong>barricade bag</strong> (purpose-built shooting support for props &mdash; not a hunting day-pack or everyday backpack standing in for a bag)</li>
                        <li>A <strong>bipod</strong> and/or rear bag for prone and supported shots (a bipod is often needed on at least some stages)</li>
                        <li>Eye and ear protection</li>
                    </ul>
                    <p>
                        Most importantly, you need safe firearm handling and a willingness to learn.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="Do I need experience to join?">
                    <p>
                        No. PRS and PR22 are accessible to shooters of all skill levels.
                    </p>
                    <p>
                        Many clubs and matches support new shooters, and the structure of the sport allows you to learn
                        by participating and observing more experienced competitors.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What gear should I bring to my first match?">
                    <p>
                        Keep it simple for your first match and focus on the essentials.
                    </p>
                    <p>Recommended:</p>
                    <ul>
                        <li>Rifle and ammunition</li>
                        <li>Scope</li>
                        <li>Magazine(s)</li>
                        <li>
                            <strong>Barricade bag</strong> &mdash; treat this as <strong>essential</strong> for PRS-style
                            stages. You will build positions on barricades, tyres, tank traps, and awkward props; a
                            purpose-built barricade / rear support bag is what you need. A <strong>hunting pack,
                            cross-body bag, or soft “range tote” is not a substitute</strong> &mdash; it will not wedge,
                            squeeze, and stabilise the rifle the way match shooters actually shoot.
                        </li>
                        <li>
                            <strong>Bipod</strong> &mdash; bring one if you have it. It is <strong>sometimes</strong>
                            the right tool for prone or low props; on other stages you will live out of your bag. Most
                            people run <strong>both</strong> a bipod and a barricade bag.
                        </li>
                        <li>Eye protection</li>
                        <li>Ear protection</li>
                        <li>Water</li>
                        <li>Weather-appropriate clothing</li>
                        <li>Sun protection such as a cap, sunscreen, and sleeves if needed</li>
                    </ul>
                    <p>
                        On the line, the <strong>barricade bag gets used constantly</strong>; the bipod comes out when
                        the stage or position calls for it. Showing up with only a hunting-style bag is a common first-match
                        mistake &mdash; bring kit built for positional shooting, not field stalking.
                    </p>
                    <p>
                        Sun protection matters. Even in winter, a full day on the range can be harsh, so bring a cap,
                        sunscreen, and clothing that gives you proper coverage.
                    </p>
                    <p>
                        You do not need advanced gear to start. Focus on safety, learning, and understanding how
                        matches work.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="Can I come watch a match?">
                    <p>
                        In most cases, yes — but it is best to check with the club or match organiser first.
                    </p>
                    <p>
                        Watching a match is one of the best ways to understand how PRS and PR22 work before competing.
                        It gives you a chance to see stage flow, equipment, and match etiquette in person.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

            {{-- Equipment --------------------------------------------------- --}}
            <x-site.faq-group
                eyebrow="Kit"
                title="Equipment"
                description="Calibres, optics, MOA vs MRAD, ballistic solvers, and the gear decisions most shooters ask about first."
            >
                <x-site.faq-item question="What are the best calibres for PRS?">
                    <p>
                        Most competitive PRS shooters use 6mm or 6.5mm calibres.
                    </p>
                    <p>These are popular because they offer:</p>
                    <ul>
                        <li>Lower recoil</li>
                        <li>Good ballistic performance</li>
                        <li>Better ability to spot impacts and misses</li>
                    </ul>
                    <p>
                        Common examples include 6mm Creedmoor, 6 Dasher, and 6.5 Creedmoor.
                    </p>
                    <p>
                        However, you do not need an optimal calibre to start. Many shooters begin with what they have
                        and upgrade over time as they gain experience.
                    </p>
                    <p>
                        <strong>6.5mm Creedmoor</strong> is a strong choice for newcomers: factory ammunition and
                        components are widely available and relatively affordable, and there is plenty of load data to
                        lean on. It carries enough energy to read misses and splashes on steel more easily than many
                        smaller or milder cartridges, while still being manageable to shoot. In practice it is a
                        well-rounded &ldquo;do everything&rdquo; option for learning PRS before chasing specialised
                        match rigs.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What optics are recommended?">
                    <p>
                        A reliable optic is critical in precision rifle shooting.
                    </p>
                    <p>Look for:</p>
                    <ul>
                        <li>Accurate and repeatable adjustments</li>
                        <li>Clear glass</li>
                        <li>Exposed turrets for dialing</li>
                        <li>A reticle suitable for holdovers</li>
                    </ul>
                    <p>
                        Most shooters use variable magnification scopes in the range of approximately 4&ndash;25x or
                        similar, depending on preference and match requirements.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="First focal plane vs second focal plane — what's the difference?">
                    <p>
                        <strong>First Focal Plane (FFP):</strong>
                        The reticle scales with magnification, meaning holdovers remain accurate at any zoom level.
                        This allows faster corrections during a stage.
                    </p>
                    <p>
                        <strong>Second Focal Plane (SFP):</strong>
                        The reticle stays the same size, and holdovers are only accurate at a specific magnification.
                    </p>
                    <p>
                        In PRS-style shooting, most competitors prefer FFP because stages often require quick
                        adjustments without changing magnification.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="MOA vs MRAD (mils) — what should I buy for PRS?">
                    <p>
                        <strong>MOA</strong> (minute of angle) and <strong>MRAD</strong> (milliradians, usually called
                        &ldquo;mils&rdquo; on turrets and reticles) are two different ways of dividing clicks and
                        subtensions. Both are perfectly usable if you commit to one system and learn it properly.
                    </p>
                    <p>
                        In practice, <strong>most PRS shooters worldwide run MRAD scopes</strong>. Squad chatter,
                        coaching, and quick corrections on the clock are overwhelmingly in tenths of a mil: wind calls,
                        elevation changes, and &ldquo;spotter language&rdquo; all line up faster when everyone shares the
                        same angular unit.
                    </p>
                    <p>
                        If you are <strong>still choosing an optic for PRS</strong>, MRAD is usually the easier match to
                        how matches are taught and discussed: the mental arithmetic lines up neatly with metric thinking
                        (for example 0.1&nbsp;mil clicks), and you spend less time translating when someone gives you a
                        correction in mils.
                    </p>
                    <p>
                        Ballistically, <strong>similar calibres are often remarkably close in angular space</strong>
                        once you are talking mils on target &mdash; what changes is mainly velocity and drop, not the
                        language the squad uses for a correction. MOA shooters absolutely still compete; the point is
                        that MRAD tends to fit the <em>conversation</em> of modern PRS with less friction.
                    </p>
                </x-site.faq-item>

                <x-site.faq-item question="What ballistic calculators or weather tools do PRS shooters use?">
                    <p>
                        You do not need the most expensive solver on day one, but you <strong>do</strong> need a
                        repeatable way to build a drop chart, confirm truing, and read wind &mdash; whether that is an
                        app, a dedicated meter, or both.
                    </p>
                    <p>
                        <strong>Phone and tablet apps</strong> (examples shooters commonly use) include
                        <strong>Hornady 4DOF</strong>, <strong>Applied Ballistics Quantum</strong> (AB&rsquo;s current
                        mobile solver with profile management and Bluetooth links to supported hardware), and
                        <strong>GeoBallistics</strong> (standalone ballistics platform with its own app ecosystem).
                    </p>
                    <p>
                        <strong>Kestrel</strong> handheld weather meters with Applied Ballistics firmware remain a
                        <strong>field-proven</strong> choice: wind, air density, and a trusted solver in one unit you can
                        keep on a tripod or in a pocket between stages.
                    </p>
                    <p>
                        <strong>Calypso</strong> builds compact <strong>ultrasonic wind meters</strong>, including
                        Applied Ballistics&ndash;oriented models that stream wind data over Bluetooth into compatible
                        apps &mdash; a lightweight way to feed live wind into an AB-style workflow without carrying a
                        full weather station.
                    </p>
                    <p>
                        <strong>Vortex</strong> also now offers ballistic <strong>weather meters</strong> such as the
                        <strong>ACE</strong>, which combine environmental sensors with <strong>GeoBallistics</strong>
                        integration (solver, profiles, and app connectivity in the GeoBallistics family). That gives
                        shooters another credible option alongside traditional Kestrel + app setups.
                    </p>
                    <p>
                        PPRC does not endorse a single brand; models and firmware change. Use this list as a map of what
                        experienced shooters mean when they say &ldquo;4DOF&rdquo;, &ldquo;AB Quantum&rdquo;,
                        &ldquo;Kestrel with AB&rdquo;, &ldquo;Calypso into the app&rdquo;, or &ldquo;GeoBallistics on the
                        Vortex meter&rdquo; &mdash; then pick what fits your budget, your phone, and how you like to work
                        on the range.
                    </p>
                </x-site.faq-item>
            </x-site.faq-group>

